// Flat index: { name, char, cat, keys } with lowercase keyword haystack.
export const EMOJI_INDEX = EMOJI_CATEGORIES.flatMap(([cat, items]) =>
  items.map(([name, char, extra]) => ({
    name, char, cat,
    keys: `${name.replace(/[_+-]/g, ' ')} ${extra || ''} ${cat}`.toLowerCase(),
  }))
);

const byName = new Map(EMOJI_INDEX.map(e => [e.name, e.char]));
export function builtinEmojiChar(name) { return byName.get(name) || null; }

// Prefix/substring search over the keyword index. Returns at most `limit` hits.
export function searchEmoji(query, limit = 60) {
  const q = query.toLowerCase().trim();
  if (!q) return EMOJI_INDEX.slice(0, limit);
  const starts = [];
  const contains = [];
  for (const e of EMOJI_INDEX) {
    if (e.name.startsWith(q)) starts.push(e);
    else if (e.keys.includes(q)) contains.push(e);
    if (starts.length >= limit) break;
  }
  return [...starts, ...contains].slice(0, limit);
}
